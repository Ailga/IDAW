                <h3>L'empathie</h3>
                    <p>Même s'il m'arrive de dire des mots malheureux, comme tout à chacun, j'ai tendance à savoir écouter et parler aux gens. Me mettre à la place de quelqu'un n'est pas un exercice difficile pour moi.</p>
                    <p>Bien évidemment, il faut savoir garder tout de même ses distances avant d'être trop vite submergé, il me semble qu'un juste milieu est toujours la meilleure des solutions</p> 
                    
